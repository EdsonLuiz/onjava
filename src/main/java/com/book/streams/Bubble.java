package com.book.streams;

public class Bubble {
  private int id;

  public Bubble(int id) {
    this.id = id;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  @Override
  public String toString() {
    return "Buble [id=" + id + "]";
  }


  
}
